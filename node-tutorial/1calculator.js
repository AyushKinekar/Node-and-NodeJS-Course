const num0 = 40;
const num1 = 2;
let syb = '*';

if (syb === '+') {
    console.log(num0 + num1);
}
else if (syb === '-') {
    console.log(num0 - num1);
}
else if (syb === '*') {
    console.log(num0 * num1);
}
else if (syb === '/') {
    console.log(num0 / num1);
}
else {
    console.log('Invalid Symbol');
}
